//Episode 5


const scenario = {
  murderer: 'Miss Scarlet',
  room: 'Kitchen',
  weapon: 'Candle Stick'
};

const changeWeapon = function(newWeapon) {
  scenario.weapon = newWeapon;
}

const declareWeapon = function() {
  return `The weapon is the ${scenario.weapon}.`;
}

changeWeapon('Revolver');
const verdict = declareWeapon();
console.log(verdict);

/*RESULT
The output is The weapon is the Revolver. Although scenario is declared with const, which cannot be changed, the variable's object can be changed. Candle Stick is changed to Revolver by the changeWeapon function.
*/


